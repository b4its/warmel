    private void getData()
    {
        // menampilkan data dari database
        try (
            Connection conn = Connections.ConnectionDB();
            PreparedStatement pstmt = conn.prepareStatement(
                "SELECT \n" +
                "    pembelian.idPembelian,\n" +
                "    pembelian.created_at ,\n" +
                "    pembelian.keterangan,\n" +
                "    pembelian.totalHarga,\n" +
                "    agen.idAgen,\n" +
                "    detail_pembelian.idDetailPembelian,\n" +
                "    detail_pembelian.idProduk,\n" +
                "    detail_pembelian.jumlah,\n" +
                "    detail_pembelian.subtotal,\n" +
                "    produk.namaProduk,\n" +
                "    produk.stok\n" + // asumsikan kamu ingin stok produk juga
                "FROM pembelian\n" +
                "JOIN agen ON pembelian.idAgen = agen.idAgen\n" +
                "JOIN detail_pembelian ON pembelian.idPembelian = detail_pembelian.idPembelian\n" +
                "JOIN produk ON detail_pembelian.idProduk = produk.idProduk WHERE pembelian.idPembelian = ?"
            );
        ) {
            String idPengeluaran = menuKeuangan.getIdPengeluaran();
            System.out.println("ID Pengeluaran: " + idPengeluaran);

            pstmt.setString(1, idPengeluaran);

            try (ResultSet rs = pstmt.executeQuery()) {
                while (rs.next()) {
                    String created_at = rs.getString("created_at");
                    String keterangan = rs.getString("keterangan");
                    double totalHargaValue = rs.getDouble("totalHarga");

                    // Parsing tanggal
                    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
                    LocalDateTime dateTime = LocalDateTime.parse(created_at, formatter);

                    int minute = dateTime.getMinute();
                    int hour = dateTime.getHour();
                    int dayOfMonth = dateTime.getDayOfMonth();
                    int month = dateTime.getMonthValue();

                    String codeCreatedAt = String.format("%02d%02d%02d%02d", minute, hour, dayOfMonth, month);
                    String codePengeluaran = "OUT" + codeCreatedAt + "-" + idPengeluaran;

                    String totalPengeluaran = formatIDCurrency.currencyFormat(totalHargaValue);

                    // Debugging
                    System.out.println("==========================");
                    System.out.println("Kode Pengeluaran: " + codePengeluaran);
                    System.out.println("Created At: " + created_at);
                    System.out.println("Total Pengeluaran: " + totalPengeluaran);

                    labelKodePengeluaran.setText(codePengeluaran);
                    areaKeterangan.setText(keterangan);
                    labelTotalHarga.setText(totalPengeluaran);  // gunakan yang sudah diformat
                }
            }

        } catch (SQLException | HeadlessException e) {
            e.printStackTrace();
        }

    }

